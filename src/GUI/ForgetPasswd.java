    private void btnGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiActionPerformed
        // TODO add your handling code here:
        if (!daGui) {
            String emailTo = txtEmailTo.getText();
            int indexOfEmail = isValid(emailTo);
            if (indexOfEmail == -1) //khong lam gì neu email khong hop le
            {
                return;
            }
            tk = list.get(indexOfEmail);
            Random rnd = new Random();
            int number = rnd.nextInt(999999);
            maXN = String.format("%06d", number);

            System.out.println(maXN);
            final String username = "adm.booksales.2023@gmail.com";
            final String password = "kevnkkyoturngzja";

            Properties prop = new Properties();
            prop.put("mail.smtp.host", "smtp.gmail.com");
            prop.put("mail.smtp.port", "587");
            prop.put("mail.smtp.auth", "true");
            prop.put("mail.smtp.starttls.enable", "true"); //TLS

            Session session = Session.getInstance(prop,
                    new javax.mail.Authenticator() {
                @Override
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(username, password);
                }
            });

            try {

                Message message = new MimeMessage(session);
                message.setFrom(new InternetAddress(username));
                message.setRecipients(
                        Message.RecipientType.TO,
                        InternetAddress.parse(emailTo)
                );
                message.setSubject("Lấy lại mật khẩu");
                message.setText("Chào bạn,"
                        + "\nMã xác nhận của bạn là " + maXN
                        + "\nKhông trả lời tin nhắn này. Xin cảm ơn!");

                Transport.send(message);
                daGui = !daGui;
                JOptionPane.showMessageDialog(this, "Gửi thành công. Vui lòng kiểm tra email để lấy mã xác nhận");
                txtEmailTo.setEnabled(false);
            } catch (MessagingException e) {
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Mã xác nhận đã được gửi vui lòng kiểm tra email");
        }
    }

