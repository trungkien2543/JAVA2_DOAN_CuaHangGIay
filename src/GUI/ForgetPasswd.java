    private int isValid(String email) {
        Matcher matcher = VALID_EMAIL_ADDRESS_REGEX.matcher(email);
        if (email == null || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập email");
            txtEmailTo.requestFocus();
            return -1;
        }

        if (matcher.matches()) {
            for (DTOTaiKhoan i : list) {
                if (i.getEmail().equals(email)) {
                    return list.indexOf(i);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng email. Vd: abc@gmail.com");
            txtEmailTo.requestFocus();
            return -1;
        }
        JOptionPane.showMessageDialog(this, "Không tồn tại email trong hệ thống!");
        return -1;
    }

